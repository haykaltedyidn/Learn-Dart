void main() {
/// Operator perbandingan digunakan untuk membandingkan dua nilai seperti samadengan == , 
/// tidak sama =! , lebih besar> , lebih besar atau sama dengan =>.

  int  number1 = 10;
  int number2 = 20;

  bool result = number1 != number2;
  print(result);

  int number3 = 15;
  int number4 = 12;

  bool result1 = number3 < number4;
  print(result1);

  int number5 = 111;
  int number6 = 222;

  bool result2 = number5 <= number6;
  print(result2);

}